void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb > 2147483647 || nb < -2147483647)
		return ;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb / 10)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}
