char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i != n)
	{
		dest[i] = src[i];
		i++;
	}
	n++;
	dest[n] = '\0';
	return (dest);
}
