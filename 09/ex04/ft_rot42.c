char	*ft_rot42(char *str)
{
	int		i;
	char	*x;

	i = 0;
	x = str;
	while (str[i] != 0)
	{
		if (str[i] >= 'A' && str[i] <= 'J')
			str[i] += 16;
		else if (str[i] >= 'K' && str[i] <= 'Z')
			str[i] -= 10;
		else if (str[i] >= 'a' && str[i] <= 'j')
			str[i] += 16;
		else if (str[i] >= 'k' && str[i] <= 'z')
			str[i] -= 10;
		i++;
	}
	return (str);
}
